    package Stack_Heap;


    public class Stack {
        public static void main(String[] args) {
            int a = 10;
            int b = 20;
            add(a,b);
        }

        public  static  void add(int a, int b){
            a=a+10;
            b=b+10;
            System.out.println(a+b);
        }
    }

